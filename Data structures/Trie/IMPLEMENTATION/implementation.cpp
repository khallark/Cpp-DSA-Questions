class Node {
public:
    char data;
    unordered_map<char, Node*> children;
    bool isTerminal;
    Node(char c) {
        this->data = c;
        this->isTerminal = false;
    }
};

class Trie {
    Node *root;
    unsigned int sizet = 0;
    void insertUtil(Node *root, string word) {
        if(word.length() == 0) {
            root->isTerminal = true;
            return;
        }
        Node *child;
        int index = word[0];
        if(root->children[index]) {
            child = root->children[index];
        }
        else {
            child = new Node(word[0]);
            root->children[index] = child;
        }
        insertUtil(child, word.substr(1));
    }

    bool searchUtil(Node *root, string word) {
        if(word.length() == 0) {
            return root->isTerminal;
        }
        int index = word[0];
        bool check = true;
        if(root->children[index]) {
            Node *child = root->children[index];
            check = searchUtil(child, word.substr(1));
        }
        else {
            return false;
        }
        return check;
    }

    void removeUtil(Node *root, string word, bool &flag) {
        if(word.length() == 0) {
            if(root->children.size() == 0) {
                delete root;
            }
            else {
                flag = true;
                root->isTerminal = false;
            }
            return;
        }
        Node *child;
        int index = word[0];
        child = root->children[index];
        removeUtil(child, word.substr(1), flag);
        if(flag) {
            return;
        }
        else {
            if(root->children.size() == 1) {
                if(root->data != '\0') delete root;
                else root->children.erase(index);
            }
            else {
                root->children.erase(index);
                flag = true;
            }
        }
    }

    void getWords(Node *root, vector<string>& ans, string output) {
        if(root->children.size() == 0) {
            output.push_back(root->data);
            ans.push_back(output);
            return;
        }
        if(root->isTerminal) {
            output.push_back(root->data);
            ans.push_back(output);
            output.pop_back();
        }
        Node *child;
        for(auto i : root->children) {
            child = i.second;
            output.push_back(root->data);
            getWords(child, ans, output);
            output.pop_back();
        }
    }

public:
    Trie() {
        root = new Node('\0');
    }

    void insertWord(string word) {
        if(!searchUtil(root, word)) {
            sizet++;
            insertUtil(root, word);
        }
    }

    bool searchWord(string word) {
        return searchUtil(root, word);
    }

    void removeWord(string word) {
        if(searchUtil(root, word)) {
            sizet--;
            bool flag = false;
            removeUtil(root, word, flag);
        }
    }

    vector<string> word_suggestions(string s) {
        vector<string> ans;
        string output = "";
        if(s == "") return ans;
        if(!root->children.count(s[0])) return ans;
        Node *temp = root->children[s[0]];
        int i = 0;
        while(i < s.size() - 1) {
            output.push_back(s[i]);
            if(!temp->children.count(s[++i])) {
                return ans;
            }
            temp = temp->children[s[i]];
        }
        
        getWords(temp, ans, output);
        return ans;
    }

    int size() {
        return sizet;
    }

    void func() {
        for(auto i : root->children) {
            cout << i.first << " ";
        }
    }
};

